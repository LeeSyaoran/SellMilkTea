    private ThuongHieu readFormForCreate() {
        String ten = safeText(txtTenThuongHieu.getText());
        if (ten.isEmpty()) throw new IllegalArgumentException("Tên thương hiệu không được để trống");
        String moTa = safeText(txtMoTa.getText());
        String ttDb = mapTrangThaiValueFromCombo(); // trả về "HoatDong"/"NgungDung"
        ThuongHieu th = new ThuongHieu();
        th.setTenThuongHieu(ten);
        th.setMoTa(moTa);
        th.setTrangThai(ttDb);
        return th;
    }
    
    private ThuongHieu readFormForUpdate() {
        Integer id = parseIdFromField();
        if (id == null) throw new IllegalArgumentException("ID không hợp lệ");
        ThuongHieu th = readFormForCreate();
        th.setId(id);
        return th;
    }
    
    private Integer parseIdFromField() {
        String s = safeText(txtMaThuongHieu.getText());
        if (s.isEmpty()) return null;
        try {
            return Integer.parseInt(s);
        } catch (NumberFormatException e) {
            return null;
        }
    }
    
    private String mapTrangThaiValueFromCombo() {
        String sel = (String) cboTrangThai.getSelectedItem();
        // Khi thêm/sửa, nếu chọn "Tất cả" -> mặc định "HoatDong"
        if (sel == null || "Tất cả".equals(sel)) return "HoatDong";
        // Hợp lệ: "HoatDong" hoặc "NgungDung"
        return sel;
    }
    
    private boolean safeInsert(ThuongHieu th) {
        try {
            // Giả định DAO trả về ID mới hoặc cập nhật vào entity; nếu không, bỏ selectRowById
            if (thuongHieuDAO instanceof dao.impl.ThuongHieuDAOImpl impl) {
                Integer newId = impl.insert(th); // điều chỉnh nếu khác chữ ký
                if (newId != null) th.setId(newId);
                return newId != null;
            } else {
                // fallback: nếu interface có insert trả boolean
                // return thuongHieuDAO.insert(th);
                // Tùy thuộc dự án của bạn, thay thế đoạn này
                return false;
            }
        } catch (NoSuchMethodError | AbstractMethodError e) {
            // Trường hợp interface khác chữ ký
            return false;
        }
    }
    
    private boolean safeUpdate(ThuongHieu th) {
        try {
            if (thuongHieuDAO instanceof dao.impl.ThuongHieuDAOImpl impl) {
                return impl.update(th); // điều chỉnh nếu chữ ký khác
            } else {
                // return thuongHieuDAO.update(th);
                return false;
            }
        } catch (NoSuchMethodError | AbstractMethodError e) {
            return false;
        }
    }
    
    private boolean safeDelete(Integer id) {
        try {
            if (thuongHieuDAO instanceof dao.impl.ThuongHieuDAOImpl impl) {
                return impl.delete(id);
            } else {
                return false;
            }
        } catch (RuntimeException re) {
            Throwable cause = re.getCause();
            if (cause instanceof SQLException) {
                String state = ((SQLException) cause).getSQLState();
                int code = ((SQLException) cause).getErrorCode();
                // Bắt lỗi ràng buộc FK phổ biến của SQL Server: 547
                if (code == 547) {
                    JOptionPane.showMessageDialog(this,
                        "Không thể xóa do đang được tham chiếu bởi dữ liệu khác (ví dụ: Đồ uống). " +
                        "Hãy cập nhật hoặc xóa dữ liệu liên quan trước, hoặc chuyển trạng thái NgungDung.");
                    return false;
                }
            }
            throw re;
        } catch (NoSuchMethodError | AbstractMethodError e) {
            return false;
        }
    }
    
    private void selectRowById(Integer id) {
        if (id == null) return;
        for (int i = 0; i < tableModel.getRowCount(); i++) {
            Object val = tableModel.getValueAt(i, 0);
            if (val != null && val.toString().equals(String.valueOf(id))) {
                tblThuongHieu.setRowSelectionInterval(i, i);
                tblThuongHieu.scrollRectToVisible(tblThuongHieu.getCellRect(i, 0, true));
                break;
            }
        }
    }

