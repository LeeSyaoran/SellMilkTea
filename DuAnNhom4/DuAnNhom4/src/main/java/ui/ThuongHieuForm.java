    private String safeText(String s) {
        return s == null ? "" : s.trim();
    }

    private void loadDataToTable(String keyword) {
        List<ThuongHieu> list = thuongHieuDAO.findAll();
        tableModel.setRowCount(0);
        int count = 0;
        String kw = safeText(keyword).toLowerCase();
        String filterTrangThai = mapTrangThaiFilter((String) cboTrangThai.getSelectedItem()); // "HoatDong"/"NgungDung"/null

        for (ThuongHieu th : list) {
            boolean matchKw = kw.isEmpty() || 
                (th.getTenThuongHieu() != null && th.getTenThuongHieu().toLowerCase().contains(kw)) ||
                (th.getMoTa() != null && th.getMoTa().toLowerCase().contains(kw));
            String ttDb = toDbTrangThai(th); // "HoatDong"/"NgungDung"
            boolean matchTrangThai = filterTrangThai == null || filterTrangThai.equals(ttDb);
            if (matchKw && matchTrangThai) {
                tableModel.addRow(new Object[]{
                    th.getId(),
                    th.getTenThuongHieu(),
                    th.getMoTa(),
                    ttDb
                });
                count++;
            }
        }
        lblTongThuongHieu.setText(String.valueOf(count));
    }

    private String mapTrangThaiFilter(String selected) {
        if (selected == null || "Tất cả".equals(selected)) return null;
        // Trả về đúng giá trị DB
        if ("HoatDong".equals(selected)) return "HoatDong";
        if ("NgungDung".equals(selected)) return "NgungDung";
        return null;
    }

    private String toDbTrangThai(ThuongHieu th) {
        Object st = th.getTrangThai();
        if (st == null) return "HoatDong";
        String txt = String.valueOf(st).trim();
        // Chuẩn hóa vài trường hợp
        if ("1".equals(txt) || "true".equalsIgnoreCase(txt) || "Đang dùng".equalsIgnoreCase(txt) || "HoatDong".equalsIgnoreCase(txt))
            return "HoatDong";
        if ("0".equals(txt) || "false".equalsIgnoreCase(txt) || "Ngừng dùng".equalsIgnoreCase(txt) || "NgungDung".equalsIgnoreCase(txt))
            return "NgungDung";
        // Mặc định về HoatDong
        return "HoatDong";
    }
