    private void tblSizeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSizeMouseClicked
        // TODO add your handling code here:
        selectedRow = tblSize.getSelectedRow();
        if (selectedRow != -1) {
            Integer id = (Integer) tblSize.getValueAt(selectedRow, 0);
            Size size = sizeDAO.findById(id);
            if (size != null) {
                setModel(size);
            }
        }
        updateButtonStates();
    }//GEN-LAST:event_tblSizeMouseClicked

