    private void init() {
        this.setLocationRelativeTo(null);
        this.setTitle("Quản Lý Size");
        tblModel = (DefaultTableModel) tblSize.getModel();
        // Cấu hình lại model table
        tblModel.setColumnIdentifiers(new Object[]{"ID", "Tên Size", "Hệ Số Giá"});
        // Ẩn cột ID
        tblSize.getColumnModel().getColumn(0).setMinWidth(0);
        tblSize.getColumnModel().getColumn(0).setMaxWidth(0);
        tblSize.getColumnModel().getColumn(0).setPreferredWidth(0);

        fillTable();
        updateButtonStates();
    }
    
    private void updateButtonStates() {
        boolean rowIsSelected = selectedRow != -1;
        boolean isManager = utils.Auth.isManager();
        btnSua.setEnabled(rowIsSelected && isManager);
    }

    private void fillTable() {
        tblModel.setRowCount(0);
        try {
            List<Size> list = sizeDAO.findAll();
            for (Size s : list) {
                tblModel.addRow(new Object[]{s.getId(), s.getTenSize(), s.getHeSoGia()});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi truy vấn dữ liệu: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void clearForm() {
        txtTenSize.setText("");
        txtHeSoGia.setText("");
        tblSize.clearSelection();
        selectedRow = -1;
        updateButtonStates();
    }

    private Size getModel() {
        // Validate
        if (txtTenSize.getText().trim().isEmpty() || txtHeSoGia.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin.", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return null;
        }

        char tenSize;
        try {
            tenSize = txtTenSize.getText().trim().toUpperCase().charAt(0);
            if (tenSize != 'S' && tenSize != 'M' && tenSize != 'L') {
                JOptionPane.showMessageDialog(this, "Tên size chỉ được là S, M, hoặc L.", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return null;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Tên size không hợp lệ.", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return null;
        }

        BigDecimal heSoGia;
        try {
            heSoGia = new BigDecimal(txtHeSoGia.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Hệ số giá phải là một con số.", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return null;
        }

        Size model = new Size();
        model.setTenSize(String.valueOf(tenSize));
        model.setHeSoGia(heSoGia);

        // Nếu đang update thì lấy ID
        if (selectedRow != -1) {
            model.setId((Integer) tblSize.getValueAt(selectedRow, 0));
        }

        return model;
    }

    private void setModel(Size model) {
        txtTenSize.setText(model.getTenSize());
        txtHeSoGia.setText(String.valueOf(model.getHeSoGia()));
    }

